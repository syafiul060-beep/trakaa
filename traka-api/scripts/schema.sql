CREATE TABLE IF NOT EXISTS users (
  id TEXT PRIMARY KEY,
  email TEXT,
  "phoneNumber" TEXT,
  "displayName" TEXT,
  "photoUrl" TEXT,
  "driverSIMVerifiedAt" TIMESTAMPTZ,
  "driverSIMNomorHash" TEXT,
  "vehicleMerek" TEXT,
  "vehicleType" TEXT,
  "vehicleJumlahPenumpang" INT,
  role TEXT,
  region TEXT,
  latitude DOUBLE PRECISION,
  longitude DOUBLE PRECISION,
  "suspendedAt" TIMESTAMPTZ,
  "suspendedReason" TEXT,
  "createdAt" TIMESTAMPTZ DEFAULT NOW(),
  "updatedAt" TIMESTAMPTZ DEFAULT NOW()
);

